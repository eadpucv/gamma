                                <p>La Facultad de Arquitectura y Diseño de la Pontificia Universidad Católica de Valparaíso,1 también conocida en Chile como la "Escuela de Arquitectura y Diseño de la Pontificia Universidad Católica de Valparaíso",2 "e[ad] Escuela de Arquitectura y Diseño",3 la "Escuela de Valparaíso",4 o simplemente "La Escuela",5 corresponde a una de la facultades de la Pontificia Universidad Católica de Valparaíso. En ella se imparten las carreras de Arquitectura, Diseño Gráfico y Diseño Industrial. Recientemente imparte programas de postgrado centrado en el "diseño náutico y marítimo" y en "ciudad y territorio".</p>
                                <p>Siendo fuertemente influenciada por las teorías de Le Corbusier, desde fines de los años 60 se establece como una de las escuelas de Arquitectura Chilena más vanguardistas e innovadoras al postular una relación directa entre la Arquitectura y la Poesía, además de asignarle un papel fundamental a la observación en la creación arquitectónica.</p>
                                <p>Es una de las escuelas de Arquitectura más antiguas de Chile, y dentro de ella se gestó la fundación de la Cooperativa Amereida. Comprende también las carreras de diseño gráfico y diseño industrial, las que son impartidas bajo el mismo punto de vista (o según lo denomina la facultad, bajo el mismo ethos) de innovación que la carrera de arquitectura.</p>
                                <p>En el año 1952, la facultad contrató al arquitecto chileno Alberto Cruz y al poeta argentino Godofredo Iommi (entre otros), quienes imprimieron su particular visión de la arquitectura, y formularon las bases, que generaron el desarrollo actual de la aproximación a la arquitectura que presenta la facultad.</p>
                                <p>En el ámbito social y político, en esta escuela se realizó la primera movilización a nivel nacional del proceso conocido como Reforma Universitaria de 1967, cuando el 15 de junio de ese año los estudiantes iniciaron una toma, con el apoyo decidido de los docentes, solicitando cogobierno, "universidad abierta" y que el rector fuere elegido por los académicos, no por la jerarquía católica. El movimiento alcanzó sus principales objetivos en pocos días, y fue replicado en muchas otras universidades del país en los años siguientes.</p>
