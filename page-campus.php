                                <p class='gris-claro'>En 1969, los profesores de la facultad, fundaron una cooperativa privada, hoy conocida como Corporación Cultural Amereida, la cual adquirió un extenso terreno en el sector de Punta de Piedra, ubicada entre las localidades de Con-cón y Quintero. Dicho terreno fue bautizado como Ciudad Abierta y se convirtió en un campo de experimentación arquitectónico, donde se construyeron (y se siguen construyendo), proyectos experimentales de arquitectura, utilizando materiales y formas poco convencionales, siendo ejecutados principalmente por los alumnos y profesores de la facultad.</p>
                                <p class='gris-claro'>Si bien, la Corporación Cultural Amereida y la facultad, en estricto rigor, son entidades independientes entre sí, la estrecha relación que poseen, dado que la mayoría de los profesores de la facultad son miembros de la corporación, y los trabajos que en la Ciudad Abierta realizan los alumnos y profesores, vuelve prácticamente imposible hablar de una sin mencionar a la otra, ya que ambas establecen una relación simbiótica.</p>
                                <p class='gris-claro'>La arquitectura realizada y fomentada por la facultad, ha producido en la "Ciudad Abierta" obras controvertidas, tanto por el uso de los materiales, como por la forma y características de los espacios creados, un ejemplo son las "Hospederías" (denominadas así por la misma facultad), cuyo estilo ha sido también catalogado como deconstructivismo, arquitectura orgánica y arquitectura regionalista, sin embargo, debido a que su génesis no aloja nada en común con estos movimientos, sino que aboga por un aproximación completamente diferente a la arquitectura, la comparación no ha podido ser fundamentada con éxito.</p>
                                <p class='gris-claro'>Ya desde 1970, la facultad, en relación con la corporación cultural, ha ejercido una influencia dentro de los círculos de arquitectura, tanto chilenos como internacionalesa, debido a su original y radical punto de vista respecto a la aproximación al diseño arquitectónico, la enseñanza de la arquitectura y la relación del arquitecto con la obra.</p>
                                <p class='gris-claro'>Ciudad Abierta es un territorio cuyo espacio es construido por sus propios habitantes a través de los oficios; siempre bajo la luz de “Amereida”, poema épico que revela una visión poética de América.</p>
                                <p class='gris-claro'>El territorio se ubica en en un sector de Ritoque llamado Punta Piedra, entre las comunas de Concón y Quintero en Valparaíso, Chile, a 4 kilómetros al norte de la desembocadura del río Aconcagua. Su extensión aproximada es de 240 hectáreas. Aparte de contar con un valor arquitectónico único, Ciudad Abierta alberga una biodiversidad en su ecosistema.</p>
                                <p class='gris-claro'>El nombre ‘Ciudad Abierta’ tiene uno de sus orígenes en la película ‘Roma ciudad abierta’ (Roma città aperta, 1945) y viene de la idea de ciudad desprotegida, en la cuál no existe una fortaleza construida, la fortaleza que la defiende se construye por medio de la hospitalidad y la palabra.</p>
